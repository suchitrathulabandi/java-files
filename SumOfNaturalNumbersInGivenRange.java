public class SumOfNaturalNumbersInGivenRange {
    public static void main(String[] args){
        int n1=5;
        int n2=10;
        int sum=0;
        for (int i=n1; i<=n2; i++){
            sum=sum+i;
}
        System.out.println("the sum of natural numbers in a given range is:" +sum);
    }
    
}
